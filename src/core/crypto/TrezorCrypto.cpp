#include <opentxs/core/crypto/TrezorCrypto.hpp>

extern "C" {
#include <trezor-crypto/bip39.h>
}

namespace opentxs
{
std::string TrezorCrypto::toWords(const OTPassword& seed) const
{
    std::string wordlist(
        ::mnemonic_from_data(
            static_cast<const uint8_t*>(seed.getMemory()),
            seed.getMemorySize()));
    return wordlist;
}

} // namespace opentxs
